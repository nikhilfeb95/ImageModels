  /**
   * Find the DMC color of the rgb values we receive.
   * 
   * @param color The DMC color whose tag we're supposed to find.
   * @return The code of the equivalent dmc color.
   */
  public static String findDmcEquivalent(int[] color) {
    if (color == null) {
      throw new IllegalArgumentException();
    }
    Map<String, Integer[]> dmcValues = DmcValues.getdmc();
    for (String keys : dmcValues.keySet()) {
      Integer[] dmcColor = dmcValues.get(keys);
      if (dmcColor[0] == color[0] && dmcColor[1] == color[1] && dmcColor[2] == color[2]) {
        return keys;
      }
    }
    return "";
  }
